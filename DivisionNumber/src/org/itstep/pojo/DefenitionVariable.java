package org.itstep.pojo;

public class DefenitionVariable {
	private int divide;
	private int divisor;
	
	public int getDivide() {
		return divide;
	}
	public void setDivide(int divide) {
		this.divide = divide;
	}
	public int getDivisor() {
		return divisor;
	}
	public void setDivisor(int divisor) {
		this.divisor = divisor;
	}
	
	public DefenitionVariable(int divide, int divisor) {
		super();
		this.divide = divide;
		this.divisor = divisor;
	}
	public DefenitionVariable() {
		
	}
}
